    /**
     * Creates new form ConnectFour
     */
    public ConnectFour() {
        
        // setting layout
        setSize (800,800);
        setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
        FlowLayout layout = new FlowLayout();
        setLayout(layout);
        GridLayout layout1 = new GridLayout(7, 7, 0, 0);
        pnlPanel.setLayout(layout1);                
        
        // loop to add each button of the array to the panel 
        for (int x = 0; x < 6; x++)
        {
            for (int y = 0; y < 7; y++)
            {
                btnArrButton [x][y] = new JButton("");
                btnArrButton [x][y].setPreferredSize(new Dimension(100, 100));
                pnlPanel.add(btnArrButton[x][y]);
            }    
        }    
        
        // adding elements to JPanel
        pnlPanel.add(lblBlank1);
        pnlPanel.add(lblBlank2);
        pnlPanel.add(lblTurn);
        pnlPanel.add(btnReset);
        pnlPanel.add(txtRecord);       
        add (pnlPanel);
        
        setVisible(true);
        
        // for loops to add ActionListener to each of the buttons in the array
        for (int x = 0; x < 6; x++)
        {
            for (int y = 0; y < 7; y++)
            {
                btnArrButton[x][y].addActionListener(connectfour);
            }
        } 
        // adds actionListener to reset button as well
        btnReset.addActionListener(connectfour);
        
        initComponents();
    }

