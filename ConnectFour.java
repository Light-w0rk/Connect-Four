    ConnectFourEvent connectfour = new ConnectFourEvent(this);
    // array of JButtons that simulate the connect 4 board 
    JButton[][] btnArrButton = new JButton[6][7];
    // new JPanel
    JPanel pnlPanel  = new JPanel();
    // static variables to be used in other class
    static JTextArea txtRecord = new JTextArea();
    static JLabel lblTurn = new JLabel();
